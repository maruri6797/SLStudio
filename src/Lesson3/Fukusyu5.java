package Lesson3;

public class Fukusyu5 {
    public static void main(String[] args) {
        int count = 0;
        count++;
        System.out.println(count);
        count++;
        System.out.println(count);
        count--;
        System.out.println(count);
        count--;
        System.out.println(count);
        count--;
        System.out.println(count);
    }
}
